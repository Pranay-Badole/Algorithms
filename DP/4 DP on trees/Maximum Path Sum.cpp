class Node
{
public:
	int data;
	Node* left;
	Node* right;

	Node(int a){
		data = a;
		left = NULL;
		right  = NULL;
	}
};

Node *root  = new Node(2);

int fun(Node* root, int& res){
	if(!root) return 0;

	int left = fun(root -> left, res);
	int right = fun(root -> right, res);

	int temp = max(root->data, root->data + max(left,right));
	int ans = root->data + left + right;

 	res = max(res,max(temp, ans));
 	return temp;
}

